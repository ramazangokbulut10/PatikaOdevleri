package day15_overloading_forloop;

public class Soru2 {
    //10 ile 30 arasındaki(10 ve 30 dahil) sayıları aralarında vrigül olarak aynı satırda yadırın

    public static void main(String[] args) {


        for (int i=10;i<=29;i++) {

            System.out.print(i+",");
        }
        System.out.print("30");

    }


}
